def download_text(url: str, label: str = "") -> str:
    """URL дан матнни юклайди."""
    print(f"  ⬇  Юкланмоқда: {label or url}")
    try:
        req = urllib.request.Request(url, headers={"User-Agent": "Mozilla/5.0"})
        with urllib.request.urlopen(req, timeout=60) as resp:
            data = resp.read()
            # Try UTF-8 first, then fallback
            for enc in ("utf-8", "utf-8-sig", "cp1251", "latin-1"):
                try:
                    return data.decode(enc)
                except UnicodeDecodeError:
                    continue
            return data.decode("utf-8", errors="replace")
    except urllib.error.HTTPError as e:
        print(f"  ⚠  HTTP хатолик {e.code}: {url}")
        return ""
    except urllib.error.URLError as e:
        print(f"  ⚠  URL хатолик: {e.reason} — {url}")
        return ""
    except Exception as e:
        print(f"  ⚠  Хатолик: {e} — {url}")
        return ""


def download_json(url: str, label: str = ""):
    """URL дан JSON маълумотни юклайди."""
    text = download_text(url, label)
    if not text:
        return None
    try:
        return json.loads(text)
    except json.JSONDecodeError as e:
        print(f"  ⚠  JSON парсинг хатолик: {e}")
        return None


def fetch_source_1() -> set:
    """anvarnarz/uzwords — лотин ёзувидаги сўзлар (~32K)"""
    print("\n📦 Манба 1: anvarnarz/uzwords")
    url = "https://raw.githubusercontent.com/anvarnarz/uzwords/master/uzwords.txt"
    text = download_text(url, "uzwords.txt")
    if not text:
        return set()
    words = set()
    for line in text.splitlines():
        w = line.strip()
        if w:
            # Лотиндан кириллга
            cyr = latin_to_cyrillic(w.lower())
            words.add(cyr)
    print(f"  ✓  {len(words)} та сўз олинди")
    return words


