def fetch_source_4() -> set:
    """LDKR-Group/UzWordnet — JSON форматда"""
    print("\n📦 Манба 4: LDKR-Group/UzWordnet")
    url = "https://raw.githubusercontent.com/LDKR-Group/UzWordnet/master/files/uzwordnet.json"
    data = download_json(url, "uzwordnet.json")
    if not data:
        return set()
    words = set()

    def extract_words_recursive(obj):
        """JSON структурадан сўзларни рекурсив ажратиб олади."""
        if isinstance(obj, str):
            for token in re.split(r'[,;/\s]+', obj):
                token = token.strip().strip('"\'()[]{}').lower()
                if len(token) >= 2 and token.isalpha():
                    if is_cyrillic(token):
                        words.add(token)
                    elif is_latin(token):
                        words.add(latin_to_cyrillic(token))
        elif isinstance(obj, dict):
            for key, val in obj.items():
                # Калит сўзларни ажратиб олиш
                if key.lower() in ("word", "lemma", "name", "title", "uz",
                                   "uzbek", "text", "value", "entry",
                                   "words", "synonyms", "synonym",
                                   "definition", "gloss"):
                    extract_words_recursive(val)
                elif isinstance(val, (dict, list)):
                    extract_words_recursive(val)
        elif isinstance(obj, list):
            for item in obj:
                extract_words_recursive(item)

    extract_words_recursive(data)
    print(f"  ✓  {len(words)} та сўз олинди")
    return words


