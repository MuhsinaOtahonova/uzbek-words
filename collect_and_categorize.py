# Кирилл ҳарфлар
CYRILLIC_LETTERS = list("абвгдеёжзийклмнопрстуфхцчшэюяўғқҳ")


def get_first_letter(word: str) -> str:
    """Сўзнинг биринчи кирилл ҳарфини қайтаради."""
    w = word.strip().lower()
    for ch in w:
        if ch in CYRILLIC_LETTERS:
            return ch
    return ""


def write_output(categorized: dict, output_dir: str):
    """Категорияланган сўзларни файлларга ёзади."""
    print(f"\n📁 Чиқиш папкаси: {output_dir}")

    stats = {}

    for cat_name, words in categorized.items():
        cat_dir = os.path.join(output_dir, cat_name)
        os.makedirs(cat_dir, exist_ok=True)

        # Ҳарф бўйича гуруҳлаш
        letter_groups = defaultdict(list)
        for w in sorted(words):
            fl = get_first_letter(w)
            if fl:
                letter_groups[fl].append(w)

        cat_total = 0
        for letter in CYRILLIC_LETTERS:
            file_path = os.path.join(cat_dir, f"{letter}.txt")
            group = letter_groups.get(letter, [])
            with open(file_path, "w", encoding="utf-8") as f:
                for w in sorted(set(group)):
                    f.write(w + "\n")
            cat_total += len(set(group))

        stats[cat_name] = cat_total
        desc = CATEGORIES.get(cat_name, {}).get("description", cat_name)
        print(f"  📂 {cat_name}/ — {cat_total} та сўз ({desc})")

    return stats


