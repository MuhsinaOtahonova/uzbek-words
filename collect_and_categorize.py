def clean_word(word: str) -> str:
    """Сўзни тозалайди."""
    word = word.strip().lower()
    # Фақат ўзбек кирилл ҳарфлари ва дефисни қолдириш
    word = re.sub(r'[^а-яёўғқҳ\-]', '', word)
    # Бошидаги ва охиридаги дефисларни олиб ташлаш
    word = word.strip('-')
    return word


def should_exclude(word: str) -> bool:
    """Сўзни чиқариб ташлаш керакми?"""
