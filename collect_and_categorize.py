def latin_to_cyrillic(text: str) -> str:
    """Ўзбек лотин ёзувидаги матнни кириллга ўгиради."""
    result = []
    i = 0
    length = len(text)
    while i < length:
        matched = False
        # Аввал кўп ҳарфли жуфтликларни текшир (узунроқдан қисқароққа)
        for lat, cyr in _LATIN_TO_CYRILLIC_MULTI:
            lat_len = len(lat)
            if text[i:i + lat_len] == lat:
                result.append(cyr)
                i += lat_len
                matched = True
                break
        if not matched:
            ch = text[i]
            if ch in _LATIN_TO_CYRILLIC_SINGLE:
                result.append(_LATIN_TO_CYRILLIC_SINGLE[ch])
            else:
                result.append(ch)
            i += 1
    return "".join(result)


def is_latin(word: str) -> bool:
    """Сўз лотин ёзувида ёзилганми текширади."""
    latin_count = sum(1 for c in word if c.isalpha() and ord(c) < 0x0400)
    alpha_count = sum(1 for c in word if c.isalpha())
    if alpha_count == 0:
        return False
    return latin_count / alpha_count > 0.5


def is_cyrillic(word: str) -> bool:
    """Сўз кирилл ёзувида ёзилганми текширади."""
    cyr_count = sum(1 for c in word if 0x0400 <= ord(c) <= 0x04FF)
    alpha_count = sum(1 for c in word if c.isalpha())
    if alpha_count == 0:
        return False
    return cyr_count / alpha_count > 0.5


