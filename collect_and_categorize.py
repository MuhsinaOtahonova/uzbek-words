def build_category_index():
    """
    Сўз → категория индексини яратади.
    Ҳар бир категориядаги сўзларни текшириш ўрнига
    тўғридан-тўғри луғат орқали излаш тезроқ.
    """
    word_to_cat = {}
    for cat_name, cat_data in CATEGORIES.items():
        if cat_name == "бошқалар":
            continue
        for w in cat_data["words"]:
            # Айрим сўзлар пробелли (масалан: "бош вазир")
            w_clean = w.strip().lower()
            if w_clean and w_clean not in word_to_cat:
                word_to_cat[w_clean] = cat_name
    return word_to_cat


def categorize_word(word: str, word_to_cat: dict) -> str:
    """Сўзни категорияга ажратади."""
    w = word.strip().lower()

    # 1) Тўғридан-тўғри мос келиш
    if w in word_to_cat:
        return word_to_cat[w]

    # 2) Суффиксни олиб ташлаб текшириш
    for suf in sorted(COMMON_SUFFIXES, key=len, reverse=True):
        if w.endswith(suf) and len(w) > len(suf) + 1:
            stem = w[:-len(suf)]
            if stem in word_to_cat:
                return word_to_cat[stem]

    return "бошқалар"


