# === LATIN → KIRIL ===
def latin_to_cyrillic(text):
    text = text.lower()
    # Multi-char first
    for lat, cyr in [("sh","ш"),("ch","ч"),("ng","нг"),("oʻ","ў"),("gʻ","ғ"),
                      ("o'","ў"),("g'","ғ"),("o`","ў"),("g`","ғ"),
                      ("yo","ё"),("ya","я"),("yu","ю"),("ye","е"),("ts","ц")]:
        text = text.replace(lat, cyr)
    for lat, cyr in [("a","а"),("b","б"),("d","д"),("e","е"),("f","ф"),
                      ("g","г"),("h","ҳ"),("i","и"),("j","ж"),("k","к"),
                      ("l","л"),("m","м"),("n","н"),("o","о"),("p","п"),
                      ("q","қ"),("r","р"),("s","с"),("t","т"),("u","у"),
                      ("v","в"),("x","х"),("y","й"),("z","з")]:
        text = text.replace(lat, cyr)
    return text

CYR = set("абвгдеёжзийклмнопрстуфхцчшэюяўғқҳъь")

def is_uzbek_cyrillic(w):
    if not w or len(w)<2 or len(w)>35: return False
    return all(c in CYR for c in w)

def fetch(url, desc=""):
    try:
        from urllib.parse import quote
        # Encode only the path portion for Cyrillic filenames
        r = Request(url, headers={"User-Agent":"UzCollector/2.0"})
        with urlopen(r, timeout=60) as resp:
            return resp.read()
    except Exception as e:
        print(f"  ❌ {desc}: {e}")
        return b""

def fetch_url(url):
    """Fetch with proper URL encoding for Cyrillic chars"""
    from urllib.parse import quote
    try:
        r = Request(url, headers={"User-Agent":"UzCollector/2.0"})
        with urlopen(r, timeout=60) as resp:
            return resp.read()
    except:
        return b""

