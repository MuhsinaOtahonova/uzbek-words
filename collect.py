def main():
    import time; t0=time.time()
    print("\n🇺🇿 ЎЗБЕК СЎЗЛАРИ ТЎПЛАГИЧ (4 манба, Кирил ёзуви)\n")
    TEMP.mkdir(parents=True, exist_ok=True)
    all_words = set()

    # === SOURCE 1: uzwords (Latin) ===
    print("📥 1/4: anvarnarz/uzwords...")
    data = fetch("https://raw.githubusercontent.com/anvarnarz/uzwords/master/uzwords.txt","uzwords")
    if data:
        for line in data.decode("utf-8","ignore").split("\n"):
            w = latin_to_cyrillic(line.strip().lower())
            if is_uzbek_cyrillic(w): all_words.add(w)
        print(f"  ✅ {len(all_words):,} сўз")

    # === SOURCE 2: kmashrab/uzbek-wordlist (Cyrillic) ===
    print("📥 2/4: kmashrab/uzbek-wordlist...")
    c0 = len(all_words)
    data = fetch("https://raw.githubusercontent.com/kmashrab/uzbek-wordlist/master/unsorted.wordlist","wordlist")
    if data:
        for line in data.decode("utf-8","ignore").split("\n"):
            w = line.strip().lower()
            if is_uzbek_cyrillic(w): all_words.add(w)
        print(f"  ✅ +{len(all_words)-c0:,} сўз")

    # === SOURCE 3: kodchi/uzbek-words (Cyrillic, per-letter) ===
    print("📥 3/4: kodchi/uzbek-words...")
    c0 = len(all_words)
    from urllib.parse import quote
    cyrillic_letters = "абвгдежзийклмнопрстуфхцчшэюяёўғқҳ"
    for letter in cyrillic_letters:
        encoded = quote(letter)
        url = f"https://raw.githubusercontent.com/kodchi/uzbek-words/master/{encoded}.txt"
        data = fetch_url(url)
        if data:
            for line in data.decode("utf-8","ignore").split("\n"):
                # kodchi format: "word\ttype" — take only the word
                parts = line.strip().split("\t")
                w = parts[0].strip().lower()
                if is_uzbek_cyrillic(w): all_words.add(w)
    print(f"  ✅ +{len(all_words)-c0:,} сўз")

    # === SOURCE 4: UzWordnet (XML) ===
    print("📥 4/4: LDKR-Group/UzWordnet...")
    c0 = len(all_words)
    # Try XML which has actual word data
    data = fetch("https://raw.githubusercontent.com/LDKR-Group/UzWordnet/master/files/uzwordnet.xml","uzwordnet.xml")
    if data:
        text = data.decode("utf-8","ignore")
        # Extract lemma and writtenForm values
        for match in re.findall(r'(?:lemma|writtenForm)="([^"]+)"', text):
            w = match.strip().lower()
            if any('\u0400'<=c<='\u04FF' for c in w):
                if is_uzbek_cyrillic(w): all_words.add(w)
            else:
                w = latin_to_cyrillic(w)
                if is_uzbek_cyrillic(w): all_words.add(w)
    print(f"  ✅ +{len(all_words)-c0:,} сўз")
    print(f"\n📊 ЖАМИ: {len(all_words):,} бензерсиз сўз\n")

    # === CATEGORIZE & SAVE ===
    print("📂 Категорияларга ажратиш...\n")
    OUT.mkdir(parents=True, exist_ok=True)
    categorized = set()

    for cat_key, cat_data in CATS.items():
        cat_dir = OUT / cat_key
        cat_dir.mkdir(parents=True, exist_ok=True)
        cat_words = set()
        seed = cat_data["words"]
        for w in all_words:
            if w in seed:
                cat_words.add(w)
            else:
                for s in seed:
                    if len(s)>=3 and s in w and len(w)-len(s)<=5:
                        cat_words.add(w)
                        break
        categorized.update(cat_words)
        by_letter = defaultdict(list)
        for w in sorted(cat_words):
            fl = w[0]
            by_letter[fl].append(w)
